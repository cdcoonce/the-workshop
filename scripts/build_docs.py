def _availability(presets: tuple[str, ...], all_project_presets: set[str]) -> str:
    """Render a component's preset availability compactly ('all' when universal)."""
    if not presets:
        return "—"
    covered = set(presets)
    if all_project_presets and all_project_presets.issubset(covered):
        extra = sorted(covered - all_project_presets)
        return "all" + (f" + {', '.join(extra)}" if extra else "")
    return ", ".join(sorted(covered))


def _project_preset_names(model: DocsModel) -> set[str]:
    return {p.name for p in model.presets if not p.is_persona}


def render_skills_table(skills: list[SkillDoc], model: DocsModel) -> str:
    """One-line-per-skill summary table (name · summary · availability)."""
    project = _project_preset_names(model)
    rows = ["| Skill | Summary | Presets |", "| --- | --- | --- |"]
    for skill in skills:
        avail = _availability(skill.presets, project)
        tag = " *(override)*" if skill.overrides_core else ""
        rows.append(
            f"| `/{skill.name}`{tag} | {_escape_cell(_first_sentence(skill.description))} "
            f"| {avail} |"
        )
    return "\n".join(rows)


def render_agents_table(agents: list[AgentDoc], model: DocsModel) -> str:
    project = _project_preset_names(model)
    rows = ["| Agent | Role | Skills | Presets |", "| --- | --- | --- | --- |"]
    for agent in agents:
        skills = ", ".join(f"`{s}`" for s in agent.skills_add) or "—"
        avail = _availability(agent.presets, project)
        tag = " *(override)*" if agent.overrides_core else ""
        rows.append(f"| **{agent.name}**{tag} | `{agent.role}` | {skills} | {avail} |")
    return "\n".join(rows)


def render_hooks_table(hooks: list[HookDoc], model: DocsModel) -> str:
    project = _project_preset_names(model)
    rows = ["| Hook | Event | Summary | Presets |", "| --- | --- | --- | --- |"]
    for hook in hooks:
        events = ", ".join(f"`{e}`" for e in hook.events) or "—"
        avail = _availability(hook.presets, project)
        rows.append(
            f"| `{hook.name}` | {events} | {_escape_cell(hook.summary)} | {avail} |"
        )
    return "\n".join(rows)


def render_presets_table(presets: list[PresetDoc]) -> str:
    rows = [
        "| Preset | Kind | Skills | Agents | Conventions |",
        "| --- | --- | --- | --- | --- |",
    ]
    for preset in presets:
        kind = "persona" if preset.is_persona else "project"
        conventions = "; ".join(preset.conventions) or "—"
        rows.append(
            f"| **`{preset.name}`** | {kind} | {len(preset.skills)} | "
            f"{len(preset.agents)} | {_escape_cell(conventions)} |"
        )
    return "\n".join(rows)


def render_methodology_table(docs: list[MethodologyDoc]) -> str:
    rows = ["| Document | Summary |", "| --- | --- |"]
    for doc in docs:
        rows.append(
            f"| [`{doc.filename}`](../../core/docs/{doc.filename}) "
            f"| {_escape_cell(doc.summary)} |"
        )
    return "\n".join(rows)


def render_scripts_table(scripts: list[ScriptDoc]) -> str:
    rows = ["| Script | Purpose |", "| --- | --- |"]
    for script in scripts:
        rows.append(f"| `scripts/{script.filename}` | {_escape_cell(script.summary)} |")
    return "\n".join(rows)


def render_counts(model: DocsModel) -> str:
    """The headline count line (single source for the README banner)."""
    core_skills = sum(1 for s in model.skills if s.source == "core")
    core_agents = sum(1 for a in model.agents if a.source == "core")
    hooks = len(model.hooks)
    project = sum(1 for p in model.presets if not p.is_persona)
    persona = sum(1 for p in model.presets if p.is_persona)
    return (
        f"**{core_skills} universal skills · {core_agents} core agents · {hooks} hooks · "
        f"{project} project presets · {persona} persona plugins**"
    )


