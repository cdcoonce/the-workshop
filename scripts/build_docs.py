def generate(root: Path) -> dict[Path, str]:
    """Return {path: desired content} for every generated doc."""
    model = build_model(root)
    reference = root / "docs" / "reference"
    outputs: dict[Path, str] = {
        reference / "skills.md": render_skills_page(model),
        reference / "agents.md": render_agents_page(model),
        reference / "hooks.md": render_hooks_page(model),
        reference / "presets.md": render_presets_page(model),
        reference / "methodology.md": render_methodology_page(model),
    }

    readme_path = root / "README.md"
    if readme_path.exists():
        outputs[readme_path] = rewrite_markers(
            readme_path.read_text(encoding="utf-8"), readme_blocks(model)
        )

    wiring_path = reference / "build-and-wiring.md"
    if wiring_path.exists():
        outputs[wiring_path] = rewrite_markers(
            wiring_path.read_text(encoding="utf-8"), wiring_blocks(model)
        )

    return outputs


def write_docs(root: Path) -> list[Path]:
    """Write all generated docs to disk; return the paths written."""
    outputs = generate(root)
    for path, content in outputs.items():
        path.parent.mkdir(parents=True, exist_ok=True)
        path.write_text(content, encoding="utf-8")
    return sorted(outputs)


def check_docs(root: Path) -> list[Path]:
    """Return the paths whose on-disk content is stale relative to the model."""
    stale: list[Path] = []
    for path, content in generate(root).items():
        current = path.read_text(encoding="utf-8") if path.exists() else None
        if current != content:
            stale.append(path)
    return sorted(stale)


def main(argv: list[str] | None = None) -> int:
    argv = list(sys.argv[1:] if argv is None else argv)
    root = Path.cwd()
    if "--check" in argv:
        stale = check_docs(root)
        if stale:
            print(
                "Documentation is stale — run `make docs` and commit:", file=sys.stderr
            )
            for path in stale:
                print(f"  - {path.relative_to(root)}", file=sys.stderr)
            return 1
        print("Documentation is up to date.")
        return 0
    written = write_docs(root)
    print(f"Generated {len(written)} documentation file(s):")
    for path in written:
        print(f"  - {path.relative_to(root)}")
    return 0


if __name__ == "__main__":
    try:
        raise SystemExit(main())
    except DocsError as exc:
        print(f"ERROR: {exc}", file=sys.stderr)
        raise SystemExit(1) from exc
