def readme_blocks(model: DocsModel) -> dict[str, str]:
    """GENERATED marker blocks for the root README."""
    core_skills = [s for s in model.skills if s.source == "core"]
    preset_skills = [s for s in model.skills if s.source != "core"]
    core_agents = [a for a in model.agents if a.source == "core"]
    preset_agents = [a for a in model.agents if a.source != "core"]
    return {
        "counts": render_counts(model),
        "skills-table": render_skills_table(core_skills, model),
        "preset-skills-table": render_skills_table(preset_skills, model),
        "agents-core-table": render_agents_table(core_agents, model),
        "agents-preset-table": render_agents_table(preset_agents, model),
        "hooks-table": render_hooks_table(model.hooks, model),
        "presets-table": render_presets_table(model.presets),
        "methodology-table": render_methodology_table(model.methodology),
    }


def wiring_blocks(model: DocsModel) -> dict[str, str]:
    """GENERATED marker blocks for docs/reference/build-and-wiring.md."""
    return {
        "scripts-table": render_scripts_table(model.scripts),
        "hooks-wiring-table": render_hooks_table(model.hooks, model),
    }


