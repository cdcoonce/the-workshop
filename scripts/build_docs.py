def _catalog_header(title: str, intro: str) -> list[str]:
    return [_DO_NOT_EDIT, "", f"# {title}", "", intro, ""]


def render_skills_page(model: DocsModel) -> str:
    core = [s for s in model.skills if s.source == "core"]
    preset = [s for s in model.skills if s.source != "core"]
    lines = _catalog_header(
        "Skills Reference",
        "Every skill available in the plugin, parsed from each skill's "
        "`SKILL.md` frontmatter. Universal skills ship with every preset; preset "
        "skills ship only where their preset is installed.",
    )
    lines += ["## Universal skills", "", render_skills_table(core, model), ""]
    if preset:
        lines += ["## Preset skills", "", render_skills_table(preset, model), ""]
    lines += ["## Full descriptions", ""]
    for skill in model.skills:
        origin = "universal" if skill.source == "core" else f"`{skill.source}` preset"
        override = (
            " — overrides the core skill of the same name"
            if skill.overrides_core
            else ""
        )
        lines += [
            f"### `/{skill.name}`",
            "",
            f"*{origin}{override}*",
            "",
            skill.description,
            "",
        ]
    return "\n".join(lines).rstrip() + "\n"


def render_agents_page(model: DocsModel) -> str:
    lines = _catalog_header(
        "Agents Reference",
        "Specialized subagent roles, parsed from each agent's `AGENT.md` "
        "frontmatter. A preset agent with the same name as a core agent replaces "
        "it (override semantics, not merge).",
    )
    lines += ["## All agents", "", render_agents_table(model.agents, model), ""]
    lines += ["## Details", ""]
    for agent in model.agents:
        origin = "core" if agent.source == "core" else f"`{agent.source}` preset"
        skills = ", ".join(f"`{s}`" for s in agent.skills_add) or "none"
        lines += [
            f"### {agent.name}",
            "",
            f"*{origin} · role `{agent.role}` · skills: {skills}*",
            "",
            agent.description,
            "",
        ]
    return "\n".join(lines).rstrip() + "\n"


def render_hooks_page(model: DocsModel) -> str:
    lines = _catalog_header(
        "Hooks Reference",
        "Lifecycle hooks and the events they run on. The event column is derived "
        "from the settings wiring (the authoritative source); the summary is each "
        "hook module's docstring.",
    )
    lines += ["## All hooks", "", render_hooks_table(model.hooks, model), ""]
    lines += ["## Details", ""]
    for hook in model.hooks:
        events = ", ".join(f"`{e}`" for e in hook.events) or "not currently wired"
        matchers = ", ".join(f"`{m}`" for m in hook.matchers) or "—"
        origin = "core" if hook.source == "core" else f"`{hook.source}` preset"
        lines += [
            f"### `{hook.name}`",
            "",
            f"*{origin} · events: {events} · matcher: {matchers}*",
            "",
            hook.summary,
            "",
        ]
    return "\n".join(lines).rstrip() + "\n"


def render_presets_page(model: DocsModel) -> str:
    lines = _catalog_header(
        "Presets Reference",
        "Every preset the marketplace can install, with the skills, agents, "
        "hooks, and conventions each one ships. Project presets configure a "
        "project type; persona plugins are output-style-only.",
    )
    lines += ["## All presets", "", render_presets_table(model.presets), ""]
    lines += ["## Details", ""]
    for preset in model.presets:
        kind = "persona plugin" if preset.is_persona else "project preset"
        lines += [
            f"### `{preset.name}`",
            "",
            f"*{kind} · v{preset.version}*",
            "",
            preset.description or "_no description_",
            "",
        ]
        if preset.conventions:
            lines += ["**Conventions:**", ""]
            lines += [f"- {c}" for c in preset.conventions]
            lines += [""]
        if preset.skills:
            lines += [
                f"**Skills ({len(preset.skills)}):** "
                + ", ".join(f"`{s}`" for s in preset.skills),
                "",
            ]
        if preset.agents:
            lines += [
                f"**Agents ({len(preset.agents)}):** "
                + ", ".join(f"`{a}`" for a in preset.agents),
                "",
            ]
        if preset.hooks:
            lines += [
                f"**Hooks ({len(preset.hooks)}):** "
                + ", ".join(f"`{h}`" for h in preset.hooks),
                "",
            ]
    return "\n".join(lines).rstrip() + "\n"


def render_methodology_page(model: DocsModel) -> str:
    lines = _catalog_header(
        "Methodology Reference",
        "The working-method docs bundled into every preset under `docs/`. Each "
        "is summarized from its source file in `core/docs/`.",
    )
    lines += [render_methodology_table(model.methodology), ""]
    for doc in model.methodology:
        lines += [f"## {doc.title}", "", doc.summary, ""]
    return "\n".join(lines).rstrip() + "\n"


