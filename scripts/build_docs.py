def render_preset_readme(
    *,
    name: str,
    description: str,
    skills: list[SkillDoc],
    agents: list[AgentDoc],
    conventions: tuple[str, ...] = (),
) -> str:
    """Render a rich README for a built plugin from its shipped components.

    Kept import-light so build_preset can call it with whatever it assembled.
    """
    lines = [f"# {name}", "", description, ""]
    if conventions:
        lines += ["## Conventions", ""]
        lines += [f"- {c}" for c in conventions]
        lines += [""]
    if skills:
        lines += ["## Skills", ""]
        lines += ["| Skill | Summary |", "| --- | --- |"]
        for skill in sorted(skills, key=lambda s: s.name):
            lines.append(
                f"| `/{_escape_cell(skill.name)}` "
                f"| {_escape_cell(_first_sentence(skill.description))} |"
            )
        lines.append("")
    if agents:
        lines += ["## Agents", ""]
        lines += ["| Agent | Role | Summary |", "| --- | --- | --- |"]
        for agent in sorted(agents, key=lambda a: a.name):
            lines.append(
                f"| {_escape_cell(agent.name)} | `{_escape_cell(agent.role)}` "
                f"| {_escape_cell(_first_sentence(agent.description))} |"
            )
        lines.append("")
    lines += [
        "## CLAUDE.md Template",
        "",
        "Copy the following into your project's `CLAUDE.md` to reference this plugin:",
        "",
        "```",
        "# Project Name",
        "",
        "## Plugins",
        "",
        f"This project uses the {name} plugin for Claude Code configuration.",
        "",
        "## Methodology",
        "",
        "See plugin documentation for TDD, root cause tracing, and subagent development processes.",
        "```",
        "",
    ]
    return "\n".join(lines)


