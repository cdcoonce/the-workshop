# Abbreviations whose internal period must not be treated as a sentence end.
_ABBREVIATIONS = frozenset(
    {"e.g", "i.e", "etc", "vs", "cf", "approx", "al", "no", "fig", "eq", "ca"}
)


def _first_sentence(text: str) -> str:
    """Return the first sentence of a description for summary tables.

    A period ends the sentence only when the word before it isn't a known
    abbreviation ("e.g.", "etc.") or a bare number/letter ("Python 3." /
    "point A."), so summaries aren't truncated mid-clause.
    """
    text = " ".join(text.split())
    for match in re.finditer(r"[.!?](\s|$)", text):
        end = match.start()
        token = text[:end].rsplit(" ", 1)[-1].rstrip(".").lower()
        if token in _ABBREVIATIONS:
            continue
        if len(token) <= 1 and token.isalnum():
            continue
        return text[: end + 1].strip()
    return text


def _escape_cell(text: str) -> str:
    """Escape a value for a Markdown table cell (pipes, newlines)."""
    return " ".join(text.split()).replace("|", "\\|")


def _load_frontmatter(md_path: Path, label: str) -> dict:
    """Parse frontmatter or raise a DocsError naming the offending file."""
    frontmatter = _parse_frontmatter(md_path.read_text(encoding="utf-8"))
    if frontmatter is None:
        raise DocsError(f"{label} '{md_path}' has no valid frontmatter")
    return frontmatter


def _require_str(frontmatter: dict, field_name: str, label: str) -> str:
    """Return a required string field, or raise DocsError naming the file.

    Guards against the frontmatter parser returning a non-string (e.g. a dict
    when a plain multiline value's continuation line contains a colon) — that
    must fail loudly rather than ship a ``{...}`` repr in the docs.
    """
    value = frontmatter.get(field_name)
    if not isinstance(value, str) or not value.strip():
        raise DocsError(f"{label} missing or non-string field '{field_name}'")
    return value.strip()


def _parse_skill(skill_dir: Path, source: str) -> SkillDoc:
    """Parse one skill directory into a SkillDoc, failing fast on bad metadata."""
    skill_md = skill_dir / "SKILL.md"
    if not skill_md.exists():
        raise DocsError(f"Skill '{skill_dir.name}' has no SKILL.md at {skill_md}")
    frontmatter = _load_frontmatter(skill_md, "Skill")
    label = f"Skill '{skill_dir.name}/SKILL.md'"
    return SkillDoc(
        name=_require_str(frontmatter, "name", label),
        description=_require_str(frontmatter, "description", label),
        source=source,
    )


def _parse_agent(agent_dir: Path, source: str) -> AgentDoc:
    """Parse one agent directory into an AgentDoc, failing fast on bad metadata."""
    agent_md = agent_dir / "AGENT.md"
    if not agent_md.exists():
        raise DocsError(f"Agent '{agent_dir.name}' has no AGENT.md at {agent_md}")
    frontmatter = _load_frontmatter(agent_md, "Agent")
    label = f"Agent '{agent_dir.name}/AGENT.md'"
    skills_config = frontmatter.get("skills", {})
    skills_add: tuple[str, ...] = ()
    if isinstance(skills_config, dict):
        add = skills_config.get("add", [])
        if isinstance(add, list):
            skills_add = tuple(str(s) for s in add)
    return AgentDoc(
        name=_require_str(frontmatter, "name", label),
        description=_require_str(frontmatter, "description", label),
        role=_require_str(frontmatter, "role", label),
        skills_add=skills_add,
        source=source,
    )


def _parse_hook_summary(hook_path: Path) -> str:
    """Return the first line of a hook module's docstring, failing fast if absent."""
    try:
        tree = ast.parse(hook_path.read_text(encoding="utf-8"))
    except SyntaxError as exc:
        raise DocsError(
            f"Hook '{hook_path.name}' is not parseable Python: {exc}"
        ) from exc
    docstring = ast.get_docstring(tree)
    if not docstring:
        raise DocsError(
            f"Hook '{hook_path.name}' has no module docstring; the first line "
            "documents what the hook does and is required for the reference."
        )
    return docstring.strip().split("\n", 1)[0].strip()


def _claimed_event(summary: str) -> str | None:
    """Extract a leading '<Token> hook:' token from a hook summary, if present."""
    match = re.match(r"([A-Za-z][\w-]*)\s+hook:", summary)
    return match.group(1) if match else None


def _script_from_command(command: str) -> str | None:
    """Extract the hook script filename from a settings hook command string."""
    match = _HOOK_SCRIPT_RE.search(command)
    return match.group(1) if match else None


def _walk_hook_wiring(settings: dict) -> dict[str, dict[str, set[str]]]:
    """Map hook script name -> {"events": {...}, "matchers": {...}} from settings."""
    wiring: dict[str, dict[str, set[str]]] = {}
    for event, entries in settings.get("hooks", {}).items():
        if not isinstance(entries, list):
            continue
        for entry in entries:
            matcher = entry.get("matcher", "")
            for hook in entry.get("hooks", []):
                script = _script_from_command(hook.get("command", ""))
                if not script:
                    continue
                record = wiring.setdefault(script, {"events": set(), "matchers": set()})
                record["events"].add(event)
                if matcher:
                    record["matchers"].add(matcher)
    return wiring


def _merged_settings_for(preset_dir: Path, base_settings_path: Path) -> dict:
    """Return the settings a preset actually ships (base-merged or preset-only)."""
    # Local import avoids a circular import: build_preset imports this module's
    # renderers for its dist README.
    from scripts.build_preset import _merge_settings

    manifest = _read_json(preset_dir / "manifest.json")
    preset_settings_path = preset_dir / "settings-preset.json"
    if manifest.get("base_settings", True):
        return _merge_settings(base_settings_path, preset_settings_path)
    if preset_settings_path.exists():
        return _read_json(preset_settings_path)
    return {}


def _read_json(path: Path) -> dict:
    import json

    try:
        return json.loads(path.read_text(encoding="utf-8"))
    except FileNotFoundError as exc:
        raise DocsError(f"Expected JSON not found: {path}") from exc
    except ValueError as exc:
        raise DocsError(f"Invalid JSON in {path}: {exc}") from exc


def _parse_methodology(doc_path: Path) -> MethodologyDoc:
    """Parse a methodology doc's title (first heading) and first paragraph."""
    lines = doc_path.read_text(encoding="utf-8").split("\n")
    title = doc_path.stem
    summary = ""
    body: list[str] = []
    for line in lines:
        if line.startswith("#") and title == doc_path.stem:
            title = line.lstrip("#").strip()
            continue
        if line.strip() and not line.startswith("#"):
            body.append(line.strip())
        elif body:
            break
    if body:
        summary = " ".join(body)
    return MethodologyDoc(filename=doc_path.name, title=title, summary=summary)


