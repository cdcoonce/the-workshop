def _preset_dirs(root: Path) -> list[Path]:
    return sorted(
        d
        for d in (root / "presets").iterdir()
        if d.is_dir() and (d / "manifest.json").exists()
    )


_BUILD_ARTIFACT_DIRS = frozenset({"__pycache__", ".pytest_cache", ".ruff_cache"})


def _is_build_artifact(path: Path, base: Path) -> bool:
    relative = path.relative_to(base)
    return bool(_BUILD_ARTIFACT_DIRS.intersection(relative.parts)) or path.suffix == ".pyc"


def is_leftover_artifact_dir(component_dir: Path) -> bool:
    """True when a directory's whole content is build artifacts git left behind.

    Switching off a branch that added a component removes its tracked files but
    keeps ignored ones — git will not delete ignored content. The empty shell
    that remains is not a malformed component, it is debris, and reporting it as
    a component names something that does not exist on the current branch.

    An *empty* directory is deliberately not treated as debris: git never
    creates one on checkout, so it can only be hand-made, and that is a real
    mistake worth failing on.
    """
    files = [p for p in component_dir.rglob("*") if p.is_file()]
    return bool(files) and all(_is_build_artifact(p, component_dir) for p in files)


def _component_dirs(parent: Path) -> list[Path]:
    """Component directories under `parent`, skipping build-artifact debris."""
    if not parent.exists():
        return []
    return sorted(
        d for d in parent.iterdir() if d.is_dir() and not is_leftover_artifact_dir(d)
    )


def _core_skill_names(root: Path) -> list[str]:
    return [d.name for d in _component_dirs(root / "core" / "skills")]


def _core_agent_names(root: Path) -> list[str]:
    return [d.name for d in _component_dirs(root / "core" / "agents")]


def _shipped_skills(manifest: dict, core_skills: list[str]) -> list[str]:
    setting = manifest.get("core", {}).get("skills", "all")
    shipped = list(core_skills) if setting == "all" else list(setting)
    shipped += [s for s in manifest.get("preset_skills", []) if s not in shipped]
    excluded = {
        e.split("/", 1)[1]
        for e in manifest.get("exclude", [])
        if e.startswith("skills/")
    }
    return sorted(s for s in shipped if s not in excluded)


def _shipped_agents(manifest: dict, core_agents: list[str]) -> list[str]:
    setting = manifest.get("core", {}).get("agents", "all")
    shipped = list(core_agents) if setting == "all" else list(setting)
    shipped += [a for a in manifest.get("preset_agents", []) if a not in shipped]
    excluded = {
        e.split("/", 1)[1]
        for e in manifest.get("exclude", [])
        if e.startswith("agents/")
    }
    return sorted(a for a in shipped if a not in excluded)


def _shipped_hooks(manifest: dict) -> list[str]:
    hooks = list(manifest.get("core", {}).get("hooks", []))
    hooks += [h for h in manifest.get("preset_hooks", []) if h not in hooks]
    # Hooks are excluded as `hooks/scripts/<name>` — the form build_preset
    # validates against. Without this filter the generated tables and the dist
    # README claim a preset ships a hook the build never copies.
    excluded = {
        e.split("/")[-1]
        for e in manifest.get("exclude", [])
        if e.startswith("hooks/scripts/")
    }
    return sorted(h for h in hooks if h not in excluded)


def build_model(root: Path) -> DocsModel:
    """Parse core + presets into the full documentation model."""
    model = DocsModel()
    core_skills = _core_skill_names(root)
    core_agents = _core_agent_names(root)
    base_settings_path = root / "core" / "settings-base.json"
    preset_dirs = _preset_dirs(root)

    # Per-preset manifests + inventories, and the inverse skill/agent -> presets map.
    manifests: dict[str, dict] = {}
    presets_with_skill: dict[str, set[str]] = {}
    presets_with_agent: dict[str, set[str]] = {}
    presets_with_hook: dict[str, set[str]] = {}
    for preset_dir in preset_dirs:
        manifest = _read_json(preset_dir / "manifest.json")
        manifests[preset_dir.name] = manifest
        for skill in _shipped_skills(manifest, core_skills):
            presets_with_skill.setdefault(skill, set()).add(preset_dir.name)
        for agent in _shipped_agents(manifest, core_agents):
            presets_with_agent.setdefault(agent, set()).add(preset_dir.name)
        for hook in _shipped_hooks(manifest):
            presets_with_hook.setdefault(hook, set()).add(preset_dir.name)

    # Skills: core, then preset-owned skills (overrides + preset-only).
    seen_skill_names: set[str] = set()
    for name in core_skills:
        skill = _parse_skill(root / "core" / "skills" / name, "core")
        model.skills.append(
            SkillDoc(
                name=skill.name,
                description=skill.description,
                source="core",
                presets=tuple(sorted(presets_with_skill.get(name, set()))),
            )
        )
        seen_skill_names.add(name)
    for preset_dir in preset_dirs:
        for skill_dir in _component_dirs(preset_dir / "skills"):
            skill = _parse_skill(skill_dir, preset_dir.name)
            model.skills.append(
                SkillDoc(
                    name=skill.name,
                    description=skill.description,
                    source=preset_dir.name,
                    presets=tuple(
                        sorted(presets_with_skill.get(skill_dir.name, set()))
                    ),
                    overrides_core=skill_dir.name in seen_skill_names,
                )
            )
    model.skills.sort(key=lambda s: (s.source != "core", s.name))

    # Agents: core, then preset-owned.
    seen_agent_names: set[str] = set()
    for name in core_agents:
        agent = _parse_agent(root / "core" / "agents" / name, "core")
        model.agents.append(
            AgentDoc(
                name=agent.name,
                description=agent.description,
                role=agent.role,
                skills_add=agent.skills_add,
                source="core",
                presets=tuple(sorted(presets_with_agent.get(name, set()))),
            )
        )
        seen_agent_names.add(name)
    for preset_dir in preset_dirs:
        for agent_dir in _component_dirs(preset_dir / "agents"):
            agent = _parse_agent(agent_dir, preset_dir.name)
            model.agents.append(
                AgentDoc(
                    name=agent.name,
                    description=agent.description,
                    role=agent.role,
                    skills_add=agent.skills_add,
                    source=preset_dir.name,
                    presets=tuple(
                        sorted(presets_with_agent.get(agent_dir.name, set()))
                    ),
                    overrides_core=agent_dir.name in seen_agent_names,
                )
            )
    model.agents.sort(key=lambda a: (a.source != "core", a.name))

    # Hook wiring aggregated across every preset's shipped settings.
    wiring: dict[str, dict[str, set[str]]] = {}
    for preset_dir in preset_dirs:
        settings = _merged_settings_for(preset_dir, base_settings_path)
        for script, record in _walk_hook_wiring(settings).items():
            agg = wiring.setdefault(script, {"events": set(), "matchers": set()})
            agg["events"] |= record["events"]
            agg["matchers"] |= record["matchers"]

    # Hooks: core hook scripts, then any preset-only hook scripts (e.g. post-edit-lint).
    hook_paths: dict[str, tuple[Path, str]] = {}
    core_hooks_dir = root / "core" / "hooks"
    if core_hooks_dir.exists():
        for hook_path in sorted(core_hooks_dir.glob("*.py")):
            hook_paths[hook_path.name] = (hook_path, "core")
    for preset_dir in preset_dirs:
        hooks_dir = preset_dir / "hooks"
        if not hooks_dir.exists():
            continue
        for hook_path in sorted(hooks_dir.glob("*.py")):
            hook_paths.setdefault(hook_path.name, (hook_path, preset_dir.name))

    for name, (hook_path, source) in sorted(hook_paths.items()):
        summary = _parse_hook_summary(hook_path)
        record = wiring.get(name, {"events": set(), "matchers": set()})
        events = tuple(sorted(record["events"]))
        _cross_validate_hook(name, summary, events)
        model.hooks.append(
            HookDoc(
                name=name,
                summary=summary,
                source=source,
                events=events,
                matchers=tuple(sorted(record["matchers"])),
                presets=tuple(sorted(presets_with_hook.get(name, set()))),
            )
        )

    # Methodology docs under core/docs (project.md excluded).
    docs_dir = root / "core" / "docs"
    if docs_dir.exists():
        for doc_path in sorted(docs_dir.glob("*.md")):
            if doc_path.name == _PROJECT_DOC:
                continue
            model.methodology.append(_parse_methodology(doc_path))

    # Build/tooling scripts (module docstring first line).
    scripts_dir = root / "scripts"
    if scripts_dir.exists():
        for script_path in sorted(scripts_dir.glob("*.py")):
            if script_path.name in ("__init__.py", "__main__.py"):
                continue
            summary = _script_summary(script_path)
            if summary:
                model.scripts.append(
                    ScriptDoc(filename=script_path.name, summary=summary)
                )

    # Presets.
    for preset_dir in preset_dirs:
        manifest = manifests[preset_dir.name]
        conventions = manifest.get("conventions", [])
        if not isinstance(conventions, list) or not all(
            isinstance(c, str) for c in conventions
        ):
            raise DocsError(
                f"Preset '{preset_dir.name}' conventions must be a list of strings, "
                f"got: {conventions!r}"
            )
        model.presets.append(
            PresetDoc(
                name=manifest.get("name", preset_dir.name),
                description=str(manifest.get("description", "")).strip(),
                version=str(manifest.get("version", "0.0.0")),
                is_persona=not manifest.get("base_settings", True)
                or preset_dir.name.startswith("persona-"),
                skills=tuple(_shipped_skills(manifest, core_skills)),
                agents=tuple(_shipped_agents(manifest, core_agents)),
                hooks=tuple(_shipped_hooks(manifest)),
                conventions=tuple(conventions),
            )
        )
    model.presets.sort(key=lambda p: (p.is_persona, p.name))

    return model


def _script_summary(script_path: Path) -> str:
    try:
        tree = ast.parse(script_path.read_text(encoding="utf-8"))
    except SyntaxError:
        return ""
    docstring = ast.get_docstring(tree)
    return docstring.strip().split("\n", 1)[0].strip() if docstring else ""


def _cross_validate_hook(name: str, summary: str, events: tuple[str, ...]) -> None:
    """Fail if a hook's docstring claims an event its wiring contradicts.

    Only a *recognized* event token is checked. A descriptive prefix such as
    "Pre-edit" (protect-files) is prose, not a claimed event, and is left alone.
    """
    claimed = _claimed_event(summary)
    if claimed is None or claimed not in KNOWN_EVENTS:
        return
    if not events:
        raise DocsError(
            f"Hook '{name}' docstring claims event '{claimed}' but the hook is not "
            "wired to any event in the settings. Wire it, or drop the event name "
            "from the docstring's first line."
        )
    if claimed not in events:
        raise DocsError(
            f"Hook '{name}' docstring claims event '{claimed}' but its wiring runs "
            f"it on {', '.join(events)}. Fix the docstring or the settings so they "
            "agree."
        )


