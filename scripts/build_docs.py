"""Generate the repo's living reference documentation from component metadata.

Single source of truth for the docs is the components themselves: SKILL.md and
AGENT.md frontmatter, hook module docstrings, hook wiring in the settings JSON,
and preset ``manifest.json`` files. This module parses that metadata once into a
model, then renders it into two shapes:

* **Fully generated catalog pages** under ``docs/reference/`` — one per component
  type (``skills.md``, ``hooks.md``, ``agents.md``, ``presets.md``,
  ``methodology.md``). These carry a "do not edit" banner and are rewritten
  whole on every run.
* **Marker-injected regions** inside hand-written prose — the root ``README.md``
  and ``docs/reference/build-and-wiring.md``. Only the content between
  ``<!-- BEGIN GENERATED: <id> -->`` / ``<!-- END GENERATED: <id> -->`` markers
  is rewritten; the surrounding prose is human-owned.

Rendering is deterministic (everything sorted), so re-running is a no-op when the
tree is fresh. ``build_docs --check`` regenerates in memory and exits non-zero if
any committed output is stale — this is the drift gate the ``make test`` target
and CI enforce.

The renderers are also imported by ``build_preset._generate_readme`` so each
built plugin's dist README shares the same per-component reference shape.
"""

from __future__ import annotations

import ast
import re
import sys
from dataclasses import dataclass, field
from pathlib import Path

from scripts.smoke_test import _parse_frontmatter

# Claude Code hook events. Used to tell a hook docstring's *claimed* event
# (e.g. "Stop hook: ...") apart from a descriptive prefix (e.g. "Pre-edit
# hook: ..."). Only a claimed event that is a real event name is cross-checked
# against the wiring; a descriptive prefix is left alone.
KNOWN_EVENTS = frozenset(
    {
        "PreToolUse",
        "PostToolUse",
        "UserPromptSubmit",
        "Notification",
        "Stop",
        "SubagentStart",
        "SubagentStop",
        "SessionStart",
        "SessionEnd",
        "PreCompact",
        "ConfigChange",
    }
)

# The non-hook, non-methodology doc under core/docs that is project-specific and
# never ships in a preset; excluded from the methodology catalog too.
_PROJECT_DOC = "project.md"

# run-hook.sh <script>.py  OR  hooks/scripts/<script>.py — the two command
# shapes used across base and preset settings.
_HOOK_SCRIPT_RE = re.compile(r"(?:run-hook\.sh\s+|hooks/scripts/)([\w.-]+\.py)")

# A GENERATED marker pair, matched by id so the same id can be filled anywhere.
# The body is captured lazily and may be empty, so the initial adjacent
# BEGIN/END state (no content yet) still matches and gets filled.
_MARKER_RE = re.compile(
    r"<!-- BEGIN GENERATED: (?P<id>[A-Za-z0-9_-]+) -->\n"
    r"(?P<body>.*?)"
    r"<!-- END GENERATED: (?P=id) -->",
    re.DOTALL,
)

_DO_NOT_EDIT = (
    "<!-- Generated by scripts/build_docs.py — do not edit by hand.\n"
    "     Run `make docs` after changing a skill, hook, agent, or preset. -->"
)


class DocsError(Exception):
    """Raised when documentation generation hits malformed or inconsistent input."""


@dataclass(frozen=True)
class SkillDoc:
    """A skill's documented surface, parsed from its SKILL.md frontmatter."""

    name: str
    description: str
    source: str  # "core" or a preset name (the preset that owns an override)
    presets: tuple[str, ...] = ()  # presets that ship this skill
    overrides_core: bool = False


@dataclass(frozen=True)
class AgentDoc:
    """An agent's documented surface, parsed from its AGENT.md frontmatter."""

    name: str
    description: str
    role: str
    skills_add: tuple[str, ...]
    source: str
    presets: tuple[str, ...] = ()
    overrides_core: bool = False


@dataclass(frozen=True)
class HookDoc:
    """A hook script's documented surface: docstring summary + wired events."""

    name: str
    summary: str
    source: str  # "core" or a preset name
    events: tuple[str, ...] = ()
    matchers: tuple[str, ...] = ()
    presets: tuple[str, ...] = ()


@dataclass(frozen=True)
class MethodologyDoc:
    """A methodology doc under core/docs (title + first paragraph)."""

    filename: str
    title: str
    summary: str


@dataclass(frozen=True)
class ScriptDoc:
    """A build/tooling script under scripts/ (module docstring first line)."""

    filename: str
    summary: str


@dataclass(frozen=True)
class PresetDoc:
    """A preset's documented surface, derived from its manifest + settings."""

    name: str
    description: str
    version: str
    is_persona: bool
    skills: tuple[str, ...]
    agents: tuple[str, ...]
    hooks: tuple[str, ...]
    conventions: tuple[str, ...]


@dataclass
class DocsModel:
    """The full parsed model rendered into every doc page."""

    skills: list[SkillDoc] = field(default_factory=list)
    agents: list[AgentDoc] = field(default_factory=list)
    hooks: list[HookDoc] = field(default_factory=list)
    methodology: list[MethodologyDoc] = field(default_factory=list)
    scripts: list[ScriptDoc] = field(default_factory=list)
    presets: list[PresetDoc] = field(default_factory=list)


